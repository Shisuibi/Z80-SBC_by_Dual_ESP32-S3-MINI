//==============================================================================//
static void MatrixPrint(void) {
	Sint08 i, j;
	Sflt32 (* pMat)[XYZW] = afMatrixStack[iCurrMatrix];

	for(i = 0;i < XYZW;i++) {
		for(j = 0;j < XYZW;j++) {
			Serial.printf("%f | ", pMat[i][j]);
		}

		Serial.println();
	}

	Serial.println();
}
//------------------------------------------------------------------------------//
static void VertexPrint(void) {
	Sint08 i, j;

	for(i = 0;i < 2;i++) {
		for(j = 0;j < XYZW;j++) {
			Serial.printf("%f | ", afCalcVertex[i][j]);
		}

		Serial.println();
	}

	Serial.println();
}
//------------------------------------------------------------------------------//
static void MatrixCalc(void) {
	Sint08 i, j;
	Sflt32 (* pMat)[XYZW] = afMatrixStack[iCurrMatrix];

	for(i = 0;i < XYZW;i++) {
		afCalcVertex[1][i] = 0.0;

		for(j = 0;j < XYZW;j++) {
			afCalcVertex[1][i] += pMat[j][i] * afCalcVertex[0][j];
		}
	}
}
//------------------------------------------------------------------------------//
static void MatrixVertex(void) {
	Sint08 i, j;

	for(i = 0;i < 4;i++) {
		for(j = 0;j < XYZW;j++) {
			afCalcVertex[0][j] = afModelVertex[i][j];
		}

		MatrixCalc();
		VertexPrint();
	}
}
